// Helper functions to generate select options
export const getCountryOptions = () => {
  return countriesData
    .sort((a, b) => a.name.localeCompare(b.name))
    .map(country => ({
      value: country.alpha2.toLowerCase(),
      label: country.name
    }));
};

export const getNationalityOptions = () => {
  return countriesData
    .sort((a, b) => a.nationality.localeCompare(b.nationality))
    .map(country => ({
      value: country.nationality.toLowerCase().replace(/\s+/g, '-'),
      label: country.nationality
    }));
};

// Helper function to get nationality by country code
export const getNationalityByCountry = (countryCode: string): string | undefined => {
  const country = countriesData.find(c => c.alpha2.toLowerCase() === countryCode.toLowerCase());
  return country?.nationality;
};

// Helper function to get country by nationality
export const getCountryByNationality = (nationality: string): CountryData | undefined => {
  const normalizedNationality = nationality.toLowerCase().replace(/-/g, ' ');
  return countriesData.find(c => c.nationality.toLowerCase() === normalizedNationality);
};